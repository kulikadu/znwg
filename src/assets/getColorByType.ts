export const getColorByType = (type: string | number) => {
  switch (type) {
    case '1': //降水
      return color1
    case '2': //相态
      return color2
    case '3': //气温
      return color3
    case '4': //最高气温
      return color4
    case '5': //最低气温
      return color5
    case '6': //风速
      return color6
    case '7': //风向(无图例)
      return color7
    case '8': //云量
      return color8
    case '9': //相对湿度
      return color9
    case '10': //能见度
      return color10
    default:
      return { value: [], color: [] }
  }
}

